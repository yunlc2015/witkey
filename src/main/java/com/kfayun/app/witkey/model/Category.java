package com.kfayun.app.witkey.model;

/**
 * 类别
 *
 * @author Billy Zhang (billy_zh@126.com)
 */
public class Category {
    private int id;

    private String name;

    private int depth;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDepth() {
        return depth;
    }

    public void setDepth(int depth) {
        this.depth = depth;
    }

    
}

